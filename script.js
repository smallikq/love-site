function updateTimer() {
    const now = new Date();
    const diff = now - relationshipStartDate;
    
    const days = Math.floor(diff / (1000 * 60 * 60 * 24));
    const hours = Math.floor((diff % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((diff % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((diff % (1000 * 60)) / 1000);
    
    // –û–±–Ω–æ–≤–ª—è–µ–º —Å –∞–Ω–∏–º–∞—Ü–∏–µ–π –ø–µ—Ä–µ–≤–æ—Ä–æ—Ç–∞
    updateValueWithFlip('days', days);
    updateValueWithFlip('hours', hours);
    updateValueWithFlip('minutes', minutes);
    updateValueWithFlip('seconds', seconds);
    
    prevValues = { days, hours, minutes, seconds };
}

